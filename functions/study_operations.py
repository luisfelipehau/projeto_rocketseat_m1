def greeting(name):
    """
    Retorna uma saudação personalizada com base no nome fornecido.

    Args:
        name (str): O nome da pessoa a ser saudada.

    Returns:
        str: Uma saudação personalizada.
    """
    return f"Olá, {name}"


def double(number):
    """
    Retorna o dobro do número fornecido.

    Args:
        number (int or float): O número a ser dobrado.

    Returns:
        int or float: O dobro do número fornecido.
    """
    return number * 2
